#include <iostream>
using std::cout;

int main() {
  cout << "Receipt for Blueberry Muffins\n\n";
  cout << "Before start, get following ingredients:\n";
  cout << "- ½ cups all-purpose flour\n";
  cout << "- ¾ cup white sugar\n";
  cout << "- ½ teaspoon salt\n";
  cout << "- 2 teaspoons baking powder\n";
  cout << "- ⅓ cup vegetable oil\n";
  cout << "- 1 egg\n";
  cout << "- ⅓ cup milk, or more as needed\n";
  cout << "- 1 cup fresh blueberries\n";
  cout << "- ½ cup white sugar\n";
  cout << "- ⅓ cup all-purpose flour\n";
  cout << "- ¼ cup butter, cubed\n";
  cout << "- ½ teaspoons ground cinnamon\n\n";
  cout << "===================================\n\n";
  cout << "Preparation:\n\n";
  cout << "Step 1:\n";
  cout << "Preheat oven to 400 degrees F (200 degrees C). Grease muffin cups or line with muffin liners.\n\n";
  cout << "Step 2:\n";
  cout << "Combine 1 1/2 cups flour, 3/4 cup sugar, salt and baking powder.\n";
  cout << "Place vegetable oil into a 1 cup measuring cup; add the egg and add enough milk to reach the 1-cup mark. Mix this with flour mixture. Fold in blueberries. Fill muffin cups right to the top, and sprinkle with crumb topping mixture.\n\n";
  cout << "Step 3:\n";
  cout << "To Make Crumb Topping: Mix together 1/2 cup sugar, 1/3 cup flour, 1/4 cup butter, and 1 1/2 teaspoons cinnamon. Mix with fork, and sprinkle over muffins before baking.\n\n";
  cout << "Step 4:\n";
  cout << "Get some gloves to protect your hands from burning by oven.\n";
  cout << "Put cups to preheated oven.\n";
  cout << "Set time for 25 minutes.\n\n";
  cout << "Step 5:\n";
  cout << "Be sure, you have your protective gloves.\n";
  cout << "Turn off the oven and get cups out.\n";

  return 0;
}

